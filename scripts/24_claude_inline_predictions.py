def main():
    base = os.path.abspath(os.path.join(os.path.dirname(__file__), ".."))
    sample_path = os.path.join(base, "outputs", "models", "_claude_sample_input.csv")
    out_preds   = os.path.join(base, "outputs", "models", "claude_inline_predictions.csv")
    out_compare = os.path.join(base, "outputs", "models", "genai_comparison_metrics.csv")

    if not os.path.exists(sample_path):
        print(f"ERROR: missing {sample_path}\n"
              "Re-generate it with:  python -c \"...\" (see script docstring).")
        sys.exit(1)

    print("=" * 64)
    print("PHASE 19: CLAUDE OPUS 4.7 INLINE PREDICTIONS (Extra Credit)")
    print("=" * 64)

    sample = pd.read_csv(sample_path)
    if len(sample) != len(CLAUDE_PREDICTIONS):
        print(f"WARN: sample has {len(sample)} rows but {len(CLAUDE_PREDICTIONS)} predictions; "
              "trimming to the minimum.")
    n = min(len(sample), len(CLAUDE_PREDICTIONS))
    sample = sample.iloc[:n].copy().reset_index(drop=True)

    p_win = np.array([p for _, p, _ in CLAUDE_PREDICTIONS[:n]], dtype=float)
    pred = (p_win >= 0.5).astype(int)
    y    = sample["target_binary"].astype(int).values

    # ---- Metrics ----
    auc = roc_auc_score(y, p_win)
    acc = accuracy_score(y, pred)
    f1  = f1_score(y, pred, zero_division=0)
    pre = precision_score(y, pred, zero_division=0)
    rec = recall_score(y, pred, zero_division=0)
    cm  = confusion_matrix(y, pred)

    print(f"\nSample size: {n} (from held-out test set, seed=42)")
    print(f"Win rate in sample: {y.mean():.3f}")
    print("\n--- Claude Opus 4.7 inline predictions ---")
    print(f"  AUC        : {auc:.4f}")
    print(f"  Accuracy   : {acc:.4f}")
    print(f"  F1         : {f1:.4f}")
    print(f"  Precision  : {pre:.4f}")
    print(f"  Recall     : {rec:.4f}")
    print(f"  Confusion  : {cm.tolist()}")

    # ---- Save per-row predictions ----
    out_df = sample[["game_id", "player_id", "opponent_id",
                     "rating_diff", "time_control", "target_binary"]].copy()
    out_df["claude_p_win"] = p_win
    out_df["claude_pred"]  = pred
    out_df["claude_reason"] = [r for _, _, r in CLAUDE_PREDICTIONS[:n]]
    out_df.to_csv(out_preds, index=False)
    print(f"\nSaved per-row predictions: {out_preds}")

    # ---- Update genai_comparison_metrics.csv ----
    new_row = {
        "approach": f"Claude Opus 4.7 (inline reasoning, n={n})",
        "accuracy": acc, "precision": pre, "recall": rec, "f1": f1,
        "roc_auc": auc, "n_test_rows": n,
    }
    if os.path.exists(out_compare):
        comp = pd.read_csv(out_compare)
        # Replace any prior Claude row (re-runs)
        comp = comp[~comp["approach"].str.contains("Claude", case=False, na=False)]
        comp = pd.concat([comp, pd.DataFrame([new_row])], ignore_index=True)
    else:
        comp = pd.DataFrame([new_row])
    comp.to_csv(out_compare, index=False)
    print(f"Updated comparison table: {out_compare}")

    # Quick side-by-side print
    print("\n--- Side-by-side (held-out test set) ---")
    print(comp[["approach", "accuracy", "f1", "roc_auc", "n_test_rows"]].to_string(index=False))
    print("=" * 64)


if __name__ == "__main__":
    main()
